package com.xawl.Pojo;

public class Practicework {

    private long id;
    private long uid;
    private long type;
    private long cid;
    private long num;
    private String lname;
    private long classhours;
    private long pass;
    private java.sql.Timestamp starteddate;


    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }


    public long getUid() {
        return uid;
    }

    public void setUid(long uid) {
        this.uid = uid;
    }


    public long getType() {
        return type;
    }

    public void setType(long type) {
        this.type = type;
    }


    public long getCid() {
        return cid;
    }

    public void setCid(long cid) {
        this.cid = cid;
    }


    public long getNum() {
        return num;
    }

    public void setNum(long num) {
        this.num = num;
    }


    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }


    public long getClasshours() {
        return classhours;
    }

    public void setClasshours(long classhours) {
        this.classhours = classhours;
    }


    public long getPass() {
        return pass;
    }

    public void setPass(long pass) {
        this.pass = pass;
    }


    public java.sql.Timestamp getStarteddate() {
        return starteddate;
    }

    public void setStarteddate(java.sql.Timestamp starteddate) {
        this.starteddate = starteddate;
    }

}
