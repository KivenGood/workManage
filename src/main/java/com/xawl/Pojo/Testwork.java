package com.xawl.Pojo;

public class Testwork {

  private Integer id;
  private Integer uid;
  private Integer type;
  private Integer num;
  private String lnames;
  private Integer classhours;
  private Integer pass;
  private java.sql.Timestamp starteddate;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public Integer getUid() {
    return uid;
  }

  public void setUid(Integer uid) {
    this.uid = uid;
  }


  public Integer getType() {
    return type;
  }

  public void setType(Integer type) {
    this.type = type;
  }


  public Integer getNum() {
    return num;
  }

  public void setNum(Integer num) {
    this.num = num;
  }


  public String getLnames() {
    return lnames;
  }

  public void setLnames(String lnames) {
    this.lnames = lnames;
  }


  public Integer getClasshours() {
    return classhours;
  }

  public void setClasshours(Integer classhours) {
    this.classhours = classhours;
  }


  public Integer getPass() {
    return pass;
  }

  public void setPass(Integer pass) {
    this.pass = pass;
  }


  public java.sql.Timestamp getStarteddate() {
    return starteddate;
  }

  public void setStarteddate(java.sql.Timestamp starteddate) {
    this.starteddate = starteddate;
  }

}
