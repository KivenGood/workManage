package com.xawl.Pojo;

public class Testwork {

  private int id;
  private int uid;
  private int type;
  private int num;
  private String lnames;
  private int classhours;
  private int pass;
  private java.sql.Timestamp starteddate;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getUid() {
    return uid;
  }

  public void setUid(int uid) {
    this.uid = uid;
  }


  public int getType() {
    return type;
  }

  public void setType(int type) {
    this.type = type;
  }


  public int getNum() {
    return num;
  }

  public void setNum(int num) {
    this.num = num;
  }


  public String getLnames() {
    return lnames;
  }

  public void setLnames(String lnames) {
    this.lnames = lnames;
  }


  public int getClasshours() {
    return classhours;
  }

  public void setClasshours(int classhours) {
    this.classhours = classhours;
  }


  public int getPass() {
    return pass;
  }

  public void setPass(int pass) {
    this.pass = pass;
  }


  public java.sql.Timestamp getStarteddate() {
    return starteddate;
  }

  public void setStarteddate(java.sql.Timestamp starteddate) {
    this.starteddate = starteddate;
  }

}
